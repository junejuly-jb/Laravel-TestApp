    public function store(Request $request)
    {
        $this->validate($request, [
            'fname' => 'required',
            'lname' => 'required'
        ]);
        $student = new Student([
            'fname' => $request->get('fname'),
            'lname' => $request->get('lname')
        ]);
        $student->save();
        return redirect('/create')->with('success', 'Data added!');
    }

